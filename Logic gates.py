def AND(A, B):
    return A & B
print("Table of AND function")
print("----------------------")
print("My output 0 AND 0 is",AND(0,0))
print("My output 0 AND 1 is",AND(0,0))
print("My output 1 AND 0 is",AND(0,0)) 
print("My output 1 AND 1 is",AND(1,1))
print("----------------------") 
print()

def OR(A, B):
    return A | B
print("Table of OR function")
print("********************") 
print("output is 0 OR 1 =",OR(1,1))
print("output is 0 OR 0 =",OR(0,0))
print("output is 1 OR 1 =",OR(1,1))
print("output is 1 OR 0 =",OR(1,1))
print("********************")
print()

